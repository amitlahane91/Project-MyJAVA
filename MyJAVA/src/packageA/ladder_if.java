package packageA;

public class ladder_if {
	public static void main(String[] args) {
		
		int marks=90;
		
		if(marks>=75) {System.out.println("student is pass with distinction");}
		
		else if (marks>=60) {System.out.println("student is pass with 1st class");}
		
		else if (marks>=50) {System.out.println("student is pass with 2nd class");}
		
		else if (marks>=35) {System.out.println("student is pass");}
		
		else {System.out.println("student is failed");} 
		
		
}

}
