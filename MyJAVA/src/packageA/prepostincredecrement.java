package packageA;

public class prepostincredecrement {

	public static void main(String[] args) {

		int a=15;
		
		//post increment and pre incement
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		
		//post decrement and pre decrement
		System.out.println(a--);
		System.out.println(a);
		System.out.println(--a);
		
		
		int x=5, y=10;
		int  z=x+y;
	
		System.out.println(z);
		
		int f=12;
		System.out.println(f--);
		System.out.println(--f);
		System.out.println(f);
		System.out.println(f++);
		System.out.println(f);
		System.out.println(++f);


}
}
