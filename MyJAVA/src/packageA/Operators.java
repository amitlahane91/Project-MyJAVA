package packageA;

public class Operators {
public static void main(String[] args) {
	
	int a=20 , b=10;
	
	System.out.println("a+b="+(a+b));
	System.out.println("a-b="+(a-b));
	System.out.println("a*b="+(a*b));
	System.out.println("a/b="+(a/b));
	System.out.println(a==b);
	System.out.println(a!=b);
	System.out.println(a<b);
	System.out.println(a>b);
	System.out.println(a<=b);
	System.out.println(a>=b);
	
	
	int c=20 , d=3;
	System.out.println("c/d="+(c/d)+ "c%d="+(c%d));
	
	float x=20 , y=3.0F;
	System.out.println("x/y="+(x/y));
	
	
	
}
}
